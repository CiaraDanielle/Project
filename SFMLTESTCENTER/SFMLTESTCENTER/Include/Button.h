#include <SFML\Graphics.hpp>
#include "Include\GameState.h"

class Button
{
public:
	

	////////////////////////////////////////////////////////////
	/// \brief Construct the buttons
	///
	/// \param Gamestates to see which gamestate it is on and load those ones on start 
	///
	////////////////////////////////////////////////////////////
	Button(GameStates state);


	////////////////////////////////////////////////////////////
	/// \brief Destruct the buttons
	///
	/// \param none
	///
	////////////////////////////////////////////////////////////
	~Button();


	////////////////////////////////////////////////////////////
	/// \brief Set the size of the buttons 
	///
	/// \param Gamestates, so only set them a certain size on certain menus 
	///
	////////////////////////////////////////////////////////////
	void setButtonSize(GameStates state);


	////////////////////////////////////////////////////////////
	/// \brief Set the buttons positions 
	///
	/// \param the width and height desired and the game state
	///
	////////////////////////////////////////////////////////////
	void setButtonPositions(float width, float height, GameStates state);


	////////////////////////////////////////////////////////////
	/// \brief Draw the buttons 
	///
	/// \param The Render Window and game states 
	///
	////////////////////////////////////////////////////////////
	void draw(sf::RenderWindow &window, GameStates state);


	////////////////////////////////////////////////////////////
	/// \brief Scale the size of the button up when on certain menus 
	///
	/// \param gamestates 
	///
	////////////////////////////////////////////////////////////
	void ButtonScaleUp(GameStates state);

	////////////////////////////////////////////////////////////
	/// \brief Scale the size of the button down when on certain menus 
	///
	/// \param gamestates 
	///
	////////////////////////////////////////////////////////////
	void ButtonScaleDown(GameStates state);

	////////////////////////////////////////////////////////////
	/// \brief Set the size of the scale for up 
	///
	/// \param gamestates 
	///
	////////////////////////////////////////////////////////////
	void setButtonScaleUp(GameStates state);

	////////////////////////////////////////////////////////////
	/// \brief Set the size of the scale for down
	///
	/// \param gamestates 
	///
	////////////////////////////////////////////////////////////
	void setButtonScaleDown(GameStates state);

	// an int for the index of the selected (highlighted) option 
	int m_selectedButtonIndex;

private:

	// a Texture for buttons 
	sf::Texture m_buttonTexture;

	// an array of buttons
	sf::Sprite buttons[4];
};


